package main

import (
	"fmt"
)

func main() {
	// Variables
	var name string = "John Doe"
	var age int = 30
	var height float64 = 5.9

	// Constants
	const pi float64 = 3.14
	const gravity float64 = 9.81

	// Printing variables and constants
	fmt.Println("Name:", name)
	fmt.Println("Age:", age)
	fmt.Println("Height:", height)
	fmt.Println("Pi:", pi)
	fmt.Println("Gravity:", gravity)
}